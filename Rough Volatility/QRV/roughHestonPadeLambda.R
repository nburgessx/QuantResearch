    p1 <- b1
    p2 <- b2 + b1*q1
    p3 <- b3 + b1*q2 + b2*q1
    p4 <- g0 * q4
    y <- x^al
    h.pade <- (p1 * y + p2 * y^2 + p3 * y^3 + p4 * y^4)/(1 + 
                                                           q1 * y + q2 * y^2 + q3 * y^3 + q4 * y^4)
    return(h.pade)
  }

